public class p_2_while_loop {
    public static void main(String[] args) {
        // Syntax for "while loop"
        /*
         * while(condition){
         * // do something
         * // updation
         * }
         */

        // while loop to print "Hello World!" 10 time
        int i = 0;
        while (i < 10) {
            System.out.println("Hello World!");
            i++;
        }
    }
}
