public class p_1_for_loop {
    public static void main(String[] args) {
        // Syntax for "for loop"
        /*
         * for(initilisation; condition; updation){
         * // do something
         * }
         */

        // for loop to print "Hello World!" 10 time
        for (int i = 0; i < 10; i++) {
            System.out.println("Hello World!");
        }

    }
}